package uk.ac.ucl.rits.inform.interchange;

/**
 * Status of a patient condition (e.g. problem or infection)
 */
public enum ConditionStatus {

    /**
     * Active.
     */
    ACTIVE,
    /**
     * Save.
     */
    SAVE,
    /**
     * Resolved.
     */
    RESOLVED,
    /**
     * Deleted.
     */
    DELETED,
    /**
     * Ignore.
     */
    IGNORE
}
